print("Exercício 8: Calculadora de salário")

pagamento_por_hora = float(input("Por favor insira o valor que você ganha por hora trabalhada: "))
horas = float(input("Por favor insira o número de horas trabalhadas nesse mês: "))

salario = pagamento_por_hora * horas

print(f"Salário do mês: {salario}")