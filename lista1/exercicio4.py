print("Exercício 4: Média de 4 notas")

nota1 = float(input("Por favor insira a nota do primeiro bimestre: "))
nota2 = float(input("Por favor insira a nota do segundo bimestre: "))
nota3 = float(input("Por favor insira a nota do terceiro bimestre: "))
nota4 = float(input("Por favor insira a nota do quarto bimestre: "))

media = (nota1 + nota2 + nota3 + nota4) / 4

print(f"A média das notas bimestrais é: {media}")